# LAÇO DE REPETIÇÃO WHILE
# Enquanto uma dada condição for verdadeira (condição de parada),
# o conteúdo de seu bloco (aquele que está em recuo, indentado)
# será executado. A condição de parada pode ser uma relação simples
# ou uma expressão com diversos operadores relacionais e lógicos

contador = 1
while contador <= 10:
    print("valor do contador  é ", contador)
    contador = contador + 1
print("Fim!")
